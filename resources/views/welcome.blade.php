        <h2><font color="#BDC3C7">OPERACIONES</font></h2>

        <P align="right"><font color="white"><PRE>
            ▸ Recorrido : Implica el acceder a cada registro una única vez aunque uno o más ítems
              del registro sean procesados.
            ▸ Búsqueda: Implica la localización de un registro caracterizado por una determinada
              clave o también el acceso a todos los registros que cumplan una o más condiciones.
            ▸ Inserción: Cuando añadimos nuevos registros a la estructura.
            ▸ Eliminación: Operación de borrado de un registro de la estructura.
            ▸ Ordenación: Es la operación de clasificar los registros conforme a un orden lógico
              determinado (por ejemplo, alfabéticamente, de acuerdo a una clave de nombre, o
              numérica, de acuerdo a alguna clave de número).
            ▸ Mezcla: Es la operación de combinar dos archivos previamente ordenados en uno
              único que también lo está.
        </PRE></font></P>

        <H2><font color="#BDC3C7">TIPOS DE ESTRUCTURAS</font></H2>

        <P><font color="white"><PRE>
                ▸ Primitivas

                 ▸ Son primitivas aquellas que no están compuestas por otras
                   estructuras de datos por ejemplo, enteros, booleanos y
                   caracteres.

                ▸ No Primitivas

                 ▸ Estructuras de datos se pueden construir de una o mas
                   primitivas. Las estructuras de datos simples que
                   consideramos se construyen a partir de estructuras primitivas
                   y son: cadenas, arreglos y registros.
        </PRE></font></P>

        <H2><font color="#BDC3C7">LINEALES Y NO LINEALES</font></H2>

        <P><font color="white"><PRE> 
            Lineales:

                ▸ Derivados del concepto de secuencia (secuencias es un conjuntos de elementos entre
                  los que se establece una relación de predecesor y sucesor).
                ▸ Desde el punto de vista de la informática, existen tres estructuras lineales
                  especialmente importantes: las pilas, las colas y las listas.
                ▸ Las operaciones básicas para dichas estructuras son:
                   • crear la secuencia vacía
                   • añadir un elemento a la secuencia
                   • borrar un elemento a la secuencia
                   • consultar un elemento de la secuencia
                   • comprobar si la secuencia está vacía

                ▸ Definición

                 • Pilas: las tres operaciones actúan sobre el final de la secuencia
                 • Colas: se añade por el final y se borra y consulta por el principio
                 • Listas: las tres operaciones se realizan sobre una posición privilegiada de la secuencia, la cual puede
                   desplazarse.

            No Lineales:

                   ▸ Estructuras de datos multienlazadas. Cada elemento puede estar enlazado a
                     cualquier otro componentes.
                   ▸ En estas estructuras cada elemento puede tener varios elementos “siguientes”, lo
                     cual introduce el concepto de estructuras de ramificación.
                   ▸ Estas estructuras de datos de ramificación son llamadas grafos y arboles.
                   ▸ Un grafo es un conjunto de puntos y un conjunto de líneas, con cada línea se
                     une un punto a otro.
                   ▸ Un árbol es un grafo conexo, simple y aciclico. Un árbol no contiene ni ciclos
                     ni bucles; existe una sola arista entre cualquier par de nodos.
        </PRE></font></P>

